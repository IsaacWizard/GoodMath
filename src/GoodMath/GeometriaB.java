    private void BtnTraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTraActionPerformed
       double basm ,basM, alt, pare, por,dos;
        if(BaseM.getText().trim().isEmpty()||BaseMa.getText().trim().isEmpty() ||A.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Para calcular el area del trapecio, por favor llenar todos los datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }else{
        basm = Double.parseDouble(BaseM.getText().trim());
        basM = Double.parseDouble(BaseMa.getText().trim());
        alt = Double.parseDouble(A.getText().trim());
        double res= basM+basm;
        por=(res*alt)/2;
        
        
        Resultado.setText("츼rea del trapecio: "+String.valueOf(por));
        }
    }//GEN-LAST:event_BtnTraActionPerformed

