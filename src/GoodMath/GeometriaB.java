    private void BtnCircleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCircleActionPerformed
        double r;
     if(Radio.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Para calcular el area del circulo, por favor llenar todos los datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }else{
         int rad = Integer.parseInt(Radio.getText().trim());
       r = Avanzadas.Potencia(rad, 2);
       double area = 3.1416*r;
       String Area = Double.toString(area);
      Resultado.setText("츼rea del circulo: "+Area);
     }
    }//GEN-LAST:event_BtnCircleActionPerformed

