    private void combiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combiActionPerformed
        valorN1.setText("");
        if(!valorR.getText().trim().isEmpty()){
            if(!valorN.getText().trim().isEmpty()){
                int n = Integer.parseInt(valorN.getText().trim());
                int r = Integer.parseInt(valorR.getText().trim());
                int nf = (int) Avanzadas.Factorial(n);
                int rf = (int) Avanzadas.Factorial(r);
                int aux = (int) Avanzadas.Factorial(n-r);
                resultado = nf/(rf*aux);
                lbResultado.setText("Resultado de Combinaci贸n: "+resultado);
            }
            else{
                JOptionPane.showMessageDialog(null, "Ingrese un valor de R", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese un valor de N", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_combiActionPerformed

