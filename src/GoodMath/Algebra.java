    private void calculadora1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculadora1ActionPerformed
        double xr,x1,x2;
        DecimalFormat df= new DecimalFormat(".####");
        if(FA.getText().trim().isEmpty() || FB.getText().trim().isEmpty() || FC.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Para calcular la formula general llene todos los campos en el espacio de la formula general", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }else{
        int a = Integer.parseInt(FA.getText());
        int b = Integer.parseInt(FB.getText());
        double b1 = Avanzadas.Potencia(Integer.parseInt(FB.getText()),(Integer.parseInt(FB1.getText())));
        int c = Integer.parseInt(FC.getText());
        
        xr= Avanzadas.raiz((b1)-(4*a*c));
        if(xr >= 0){
            x1=(((-b)+xr)/(2*a));
            x2=(((-b)-xr)/(2*a));
            RX1.setText(""+df.format(x1));
            RX2.setText(""+df.format(x2));
        }else{
            JOptionPane.showMessageDialog(null, "No se puede calcular ya que es una raiz negativa");
        }
        }     
    }//GEN-LAST:event_calculadora1ActionPerformed

